// Algoritmo para mostrar el mensaje "Hola Mundo"
public class HolaMundo {
  public static void main(String[] args) {
    // Definimos la variable de entrada "text"
    String text;

    // Definimos la variable de salida "salute"
    String salute;

    // Asignamos valor inicial a la variable de entrada "text"
    text = "Hola Mundo";

    // Inicializamos la variable de salida "salute"
    salute = "";

    // Asignamos valor a la variable de salida "salute"
    // Para ello, "salute" recibir√° el valor de "text"
    salute = text;

    // Mostramos el contenido de la variable de salida "salute"
    System.out.println(salute);
  }
}
